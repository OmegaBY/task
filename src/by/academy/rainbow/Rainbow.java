package by.academy.rainbow;

public class Rainbow {

    public static String getClr(int a){
      switch (a) {
          case 1:
              return  "red";

          case 2:
              return "orange";

          case 3:
              return "yellow";

          case 4:
              return "green";

          case 5:
              return "blue";

          case 6:
              return "indigo";

          case 7:
              return "violet";

          default:
             break;
      }
    }

}
